using System.Collections.Immutable;
using System.Globalization;
using System.Text;
using DotBoxD.Plugins.Analyzer.Analysis.Lowering;
using Microsoft.CodeAnalysis;

namespace DotBoxD.Plugins.Analyzer.Analysis.InvokeAsync;

internal static class InvokeAsyncInterceptorEmitter
{
    public static void Emit(SourceProductionContext context, ImmutableArray<InvokeAsyncInterception> interceptions)
    {
        if (interceptions.IsDefaultOrEmpty)
        {
            return;
        }

        context.AddSource("DotBoxDInvokeAsyncInterceptors.g.cs", BuildInterceptors(interceptions));
    }

    private static string BuildInterceptors(ImmutableArray<InvokeAsyncInterception> interceptions)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated/>");
        builder.AppendLine("#nullable enable");
        builder.Append("namespace ").AppendLine(DotBoxDGenerationNames.TypeNames.GeneratedInterceptorsNamespace);
        builder.AppendLine("{");
        builder.AppendLine("    internal static class InvokeAsyncInterceptors");
        builder.AppendLine("    {");

        for (var i = 0; i < interceptions.Length; i++)
        {
            AppendInterceptor(builder, interceptions[i], i);
        }

        if (interceptions.Any(static interception => interception.UsesReflectionCaptures))
        {
            AppendReflectionCaptureHelpers(builder);
        }

        builder.AppendLine("    }");
        builder.AppendLine("}");
        return builder.ToString();
    }

    private static void AppendInterceptor(
        StringBuilder builder,
        InvokeAsyncInterception interception,
        int index)
    {
        builder.Append("        ").AppendLine(interception.AttributeSyntax);
        builder.Append("        internal static async global::System.Threading.Tasks.ValueTask<")
            .Append(interception.ReturnType)
            .Append("> InvokeAsync_")
            .Append(index.ToString(CultureInfo.InvariantCulture))
            .AppendLine("(");
        builder.Append("            this ").Append(interception.ReceiverType).AppendLine(" server,");
        if (interception.CaptureType is { } captureType &&
            interception.CaptureDelegateType is { } captureDelegateType)
        {
            builder.Append("            ").Append(captureType).AppendLine(" captures,");
            builder.Append("            ").Append(captureDelegateType).AppendLine(" lambda)");
        }
        else
        {
            builder.Append("            global::System.Func<")
                .Append(interception.HostAccessType)
                .Append(", ")
                .Append("global::System.Threading.Tasks.ValueTask<")
                .Append(interception.ReturnType)
                .AppendLine(">> lambda)");
        }

        builder.AppendLine("        {");
        var server = "server";
        if (interception.ServerAccessType is { } serverAccessType)
        {
            server = "__server";
            builder.Append("            var __server = server as ").Append(serverAccessType)
                .AppendLine(" ?? throw new global::System.InvalidOperationException(\"Plugin server InvokeAsync calls require a generated plugin server receiver.\");");
        }

        if (interception.CaptureType is not null)
        {
            builder.AppendLine("            global::System.ArgumentNullException.ThrowIfNull(captures);");
        }

        builder.AppendLine("            global::System.ArgumentNullException.ThrowIfNull(lambda);");
        builder.Append("            var __pluginId = await ").Append(server).Append(".Services.EnsureAnonymousKernelAsync(")
            .Append(Literal(interception.PluginId))
            .Append(", ")
            .Append(interception.PackageFullName)
            .AppendLine(".Create).ConfigureAwait(false);");
        builder.Append("            var __request = global::DotBoxD.Plugins.KernelRpcBinaryCodec.EncodeArguments(")
            .Append(interception.ArgumentsExpression)
            .AppendLine(");");
        builder.Append("            var __response = await ").Append(server)
            .AppendLine(".Services.WireClient.InvokeServerExtensionAsync(__pluginId, __request).ConfigureAwait(false);");
        builder.AppendLine("            var __result = global::DotBoxD.Plugins.KernelRpcBinaryCodec.DecodeValue(__response);");
        if (interception.SyncOutAssignments.Count > 0)
        {
            builder.AppendLine("            __result.RequireKind(global::DotBoxD.Plugins.KernelRpcValueKind.Record);");
            builder.Append("            if (__result.ItemCount != ")
                .Append((interception.SyncOutAssignments.Count + 1).ToString(CultureInfo.InvariantCulture))
                .AppendLine(")");
            builder.AppendLine("            {");
            builder.AppendLine("                throw new global::System.NotSupportedException(\"InvokeAsync response field count did not match the generated capture shape.\");");
            builder.AppendLine("            }");
            for (var i = 0; i < interception.SyncOutAssignments.Count; i++)
            {
                builder.Append("            ").Append(interception.SyncOutAssignments[i]).AppendLine(";");
            }
        }

        builder.Append("            return ").Append(interception.ResultExpression).AppendLine(";");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.Append(interception.Helpers);
    }

    private static void AppendReflectionCaptureHelpers(StringBuilder builder)
    {
        builder.AppendLine("        private static T __ReadCapture<T>(global::System.Delegate lambda, string fieldName)");
        builder.AppendLine("        {");
        builder.AppendLine("            var target = lambda.Target ?? throw new global::System.NotSupportedException(\"InvokeAsync implicit captures require a compiler-generated closure target.\");");
        builder.AppendLine("            var value = __FindCaptureField(target.GetType(), fieldName).GetValue(target);");
        builder.AppendLine("            return value is null ? default! : (T)value;");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        private static void __WriteCapture<T>(global::System.Delegate lambda, string fieldName, T value)");
        builder.AppendLine("        {");
        builder.AppendLine("            var target = lambda.Target ?? throw new global::System.NotSupportedException(\"InvokeAsync implicit captures require a compiler-generated closure target.\");");
        builder.AppendLine("            __FindCaptureField(target.GetType(), fieldName).SetValue(target, value);");
        builder.AppendLine("        }");
        builder.AppendLine();
        builder.AppendLine("        private static global::System.Reflection.FieldInfo __FindCaptureField(global::System.Type type, string fieldName)");
        builder.AppendLine("        {");
        builder.AppendLine("            for (var current = type; current is not null; current = current.BaseType)");
        builder.AppendLine("            {");
        builder.AppendLine("                var field = current.GetField(fieldName, global::System.Reflection.BindingFlags.Instance | global::System.Reflection.BindingFlags.Public | global::System.Reflection.BindingFlags.NonPublic);");
        builder.AppendLine("                if (field is not null)");
        builder.AppendLine("                {");
        builder.AppendLine("                    return field;");
        builder.AppendLine("                }");
        builder.AppendLine("            }");
        builder.AppendLine();
        builder.AppendLine("            throw new global::System.NotSupportedException(\"InvokeAsync could not find compiler capture field '\" + fieldName + \"'. Use the explicit capture-bag overload for stable capture marshalling.\");");
        builder.AppendLine("        }");
        builder.AppendLine();
    }

    private static string Literal(string value)
        => "\"" + value.Replace("\\", "\\\\").Replace("\"", "\\\"") + "\"";
}
