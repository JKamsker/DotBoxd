using System.Text;
using DotBoxD.Plugins.Analyzer.Analysis;

namespace DotBoxD.Plugins.Analyzer.Analysis.PluginServer;

internal static class PluginServerFacadeEmitter
{
    public static GeneratedPluginPackage Emit(PluginServerFacadeModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated/>");
        builder.AppendLine("#nullable enable");
        builder.AppendLine("using System.Linq;");
        builder.AppendLine();
        if (!string.IsNullOrEmpty(model.Namespace))
        {
            builder.Append("namespace ").Append(model.Namespace).AppendLine(";");
            builder.AppendLine();
        }

        AppendFacade(builder, model);
        builder.AppendLine();
        AppendServerInterface(builder, model);
        builder.AppendLine();
        PluginServerSetupEmitter.AppendBuilder(builder, model);
        PluginServerSetupEmitter.AppendSetupInterfaces(builder, model);
        return new GeneratedPluginPackage(HintName(model), builder.ToString());
    }

    private static void AppendFacade(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.Append(model.Accessibility).Append(" partial class ").Append(model.ClassName)
            .Append(" : ").AppendLine(model.ServerInterfaceName);
        builder.AppendLine("{");
        builder.AppendLine("    private const string NotStartedMessage = \"Call StartAsync() before using the server.\";");
        builder.AppendLine("    private readonly global::System.Func<global::System.Threading.CancellationToken, global::System.Threading.Tasks.ValueTask<global::DotBoxD.Services.Peer.RpcPeerSession>>? _connectionFactory;");
        builder.AppendLine("    private readonly global::System.Collections.Concurrent.ConcurrentDictionary<string, global::System.Lazy<global::System.Threading.Tasks.Task<string>>> _anonymousKernels = new();");
        builder.AppendLine("    private readonly global::System.Collections.Generic.Dictionary<global::System.Type, string> _serverExtensions = new();");
        builder.AppendLine("    private readonly global::System.Collections.Generic.List<RecordedInstall> _setupInstalls;");
        builder.Append("    private ").Append(model.ControlServiceType).AppendLine("? _control;");
        builder.Append("    private ").Append(model.WorldType).AppendLine("? _world;");
        builder.AppendLine("    private global::DotBoxD.Plugins.Runtime.RemoteHookRegistry? _hooks;");
        builder.AppendLine("    private global::DotBoxD.Plugins.Runtime.RemoteSubscriptionRegistry? _subscriptions;");
        builder.AppendLine("    private global::DotBoxD.Services.Peer.RpcPeerSession? _session;");
        foreach (var control in model.Controls)
        {
            builder.Append("    private ").Append(control.WrapperName).Append("? _")
                .Append(FieldName(control.Name)).AppendLine(";");
        }
        builder.AppendLine("    private bool _started;");
        builder.AppendLine("    private bool _setupReplayed;");
        builder.AppendLine("    private bool _configured;");
        builder.AppendLine("    private bool _disposed;");
        builder.AppendLine();
        builder.Append("    public ").Append(model.ClassName).Append('(').Append(model.ControlServiceType)
            .Append(" control, ").Append(model.WorldType).AppendLine("? world = null)");
        builder.AppendLine("        : this(control, world, setup: null)");
        builder.AppendLine("    {");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.Append("    public ").Append(model.ClassName).Append('(').Append(model.ControlServiceType)
            .Append(" control, ").Append(model.WorldType).Append("? world, global::System.Action<")
            .Append(model.SetupInterfaceName).AppendLine(">? setup)");
        builder.AppendLine("    {");
        builder.AppendLine("        global::System.ArgumentNullException.ThrowIfNull(control);");
        builder.AppendLine("        _setupInstalls = RecordSetup(setup);");
        builder.AppendLine("        Initialize(control, world);");
        builder.AppendLine("        _started = true;");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.Append("    internal ").Append(model.ClassName)
            .AppendLine("(global::System.Func<global::System.Threading.CancellationToken, global::System.Threading.Tasks.ValueTask<global::DotBoxD.Services.Peer.RpcPeerSession>> connectionFactory)");
        builder.AppendLine("        : this(connectionFactory, setup: null)");
        builder.AppendLine("    {");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.Append("    internal ").Append(model.ClassName)
            .Append("(global::System.Func<global::System.Threading.CancellationToken, global::System.Threading.Tasks.ValueTask<global::DotBoxD.Services.Peer.RpcPeerSession>> connectionFactory, global::System.Action<")
            .Append(model.SetupInterfaceName).AppendLine(">? setup)");
        builder.AppendLine("    {");
        builder.AppendLine("        _connectionFactory = connectionFactory ?? throw new global::System.ArgumentNullException(nameof(connectionFactory));");
        builder.AppendLine("        _setupInstalls = RecordSetup(setup);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    partial void OnConfigured();");
        AppendProperties(builder, model);
        AppendLifecycle(builder, model);
        AppendWorldForwarders(builder, model);
        AppendInstallSurface(builder, model);
        PluginServerSetupEmitter.AppendSetupMembers(builder, model);
        AppendLiveSettingsHandle(builder, model);
        foreach (var control in model.Controls)
        {
            PluginServerWrapperEmitter.AppendControlWrapper(builder, model, control);
        }
        builder.AppendLine("}");
    }

    private static void AppendProperties(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.AppendLine();
        builder.Append("    public ").Append(model.ServerInterfaceName).AppendLine(" Services => this;");
        builder.AppendLine("    public global::DotBoxD.Abstractions.IServerExtensionClientRegistry ServerExtensions => this;");
        builder.AppendLine("    public global::DotBoxD.Plugins.Runtime.RemoteHookRegistry Hooks => _started && _hooks is not null ? _hooks : throw new global::System.InvalidOperationException(NotStartedMessage);");
        builder.AppendLine("    public global::DotBoxD.Plugins.Runtime.RemoteSubscriptionRegistry Subscriptions => _started && _subscriptions is not null ? _subscriptions : throw new global::System.InvalidOperationException(NotStartedMessage);");
        foreach (var control in model.Controls)
        {
            builder.Append("    public ").Append(control.Type).Append(' ').Append(control.Name)
                .Append(" => _started && _").Append(FieldName(control.Name))
                .AppendLine(" is not null ? _" + FieldName(control.Name) + " : throw new global::System.InvalidOperationException(NotStartedMessage);");
        }
        builder.AppendLine("    public global::DotBoxD.Abstractions.IServerExtensionWireClient WireClient => this;");
    }

    private static void AppendServerInterface(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.Append(model.Accessibility).Append(" interface ").Append(model.ServerInterfaceName)
            .Append(" : ").Append(model.WorldType)
            .Append(", global::DotBoxD.Abstractions.IPluginServer<").Append(model.WorldType)
            .Append(">, global::DotBoxD.Abstractions.IServerExtensionClientRegistry, ")
            .AppendLine("global::System.IDisposable, global::System.IAsyncDisposable");
        builder.AppendLine("{");
        builder.Append("    ").Append(model.ServerInterfaceName).AppendLine(" Services { get; }");
        builder.AppendLine("    global::DotBoxD.Abstractions.IServerExtensionClientRegistry ServerExtensions { get; }");
        builder.AppendLine("    global::DotBoxD.Plugins.Runtime.RemoteHookRegistry Hooks { get; }");
        builder.AppendLine("    global::DotBoxD.Plugins.Runtime.RemoteSubscriptionRegistry Subscriptions { get; }");
        builder.AppendLine("    global::DotBoxD.Abstractions.IServerExtensionWireClient WireClient { get; }");
        builder.AppendLine("    global::DotBoxD.Abstractions.ILiveSettingsHandle<TKernel> Get<TKernel>() where TKernel : class, new();");
        builder.AppendLine("    global::System.Threading.Tasks.Task<string> EnsureAnonymousKernelAsync(string pluginId, global::System.Func<global::DotBoxD.Plugins.PluginPackage> factory);");
        builder.AppendLine("}");
    }

    private static void AppendLifecycle(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.AppendLine();
        builder.AppendLine("    public async global::System.Threading.Tasks.ValueTask StartAsync(global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("    {");
        builder.AppendLine("        ThrowIfDisposed();");
        builder.AppendLine("        if (!_started)");
        builder.AppendLine("        {");
        builder.AppendLine("            if (_connectionFactory is null) { throw new global::System.InvalidOperationException(NotStartedMessage); }");
        builder.AppendLine("            _session = await _connectionFactory(cancellationToken).ConfigureAwait(false);");
        builder.AppendLine("            var control = _session.Get<" + model.ControlServiceType + ">();");
        builder.AppendLine("            var world = global::DotBoxD.Services.Generated.DotBoxDGeneratedExtensions.Get" + ShortTypeName(model.WorldType) + "(_session.Peer);");
        builder.AppendLine("            Initialize(control, world);");
        builder.AppendLine("            _started = true;");
        builder.AppendLine("        }");
        builder.AppendLine("        await ReplaySetupAsync(cancellationToken).ConfigureAwait(false);");
        builder.AppendLine("        if (!_configured)");
        builder.AppendLine("        {");
        builder.AppendLine("            OnConfigured();");
        builder.AppendLine("            _configured = true;");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public async global::System.Threading.Tasks.ValueTask RunAsync(global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("    {");
        builder.AppendLine("        await StartAsync(cancellationToken).ConfigureAwait(false);");
        builder.AppendLine("        await HoldUntilShutdownAsync(cancellationToken).ConfigureAwait(false);");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    public global::System.Threading.Tasks.ValueTask HoldUntilShutdownAsync(global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("        => RequireControl().HoldUntilShutdownAsync(cancellationToken);");
        builder.AppendLine();
        builder.AppendLine("    public void Dispose() => DisposeAsync().AsTask().GetAwaiter().GetResult();");
        builder.AppendLine("    public async global::System.Threading.Tasks.ValueTask DisposeAsync()");
        builder.AppendLine("    {");
        builder.AppendLine("        if (_disposed) { return; }");
        builder.AppendLine("        _disposed = true;");
        builder.AppendLine("        if (_session is not null) { await _session.DisposeAsync().ConfigureAwait(false); }");
        builder.AppendLine("    }");
        builder.AppendLine();
        builder.AppendLine("    private void Initialize(" + model.ControlServiceType + " control, " + model.WorldType + "? world)");
        builder.AppendLine("    {");
        builder.AppendLine("        _control = control;");
        builder.AppendLine("        _world = world;");
        builder.AppendLine("        _hooks = new global::DotBoxD.Plugins.Runtime.RemoteHookRegistry(package => InstallPluginPackageAsync(package));");
        builder.AppendLine("        _subscriptions = new global::DotBoxD.Plugins.Runtime.RemoteSubscriptionRegistry(package => InstallSubscriptionPackageAsync(package));");
        foreach (var control in model.Controls)
        {
            builder.Append("        _").Append(FieldName(control.Name)).Append(" = world is null ? null : new ")
                .Append(control.WrapperName).Append("(this, world.").Append(control.Name).AppendLine(");");
        }
        builder.AppendLine("    }");
        builder.AppendLine("    private " + model.ControlServiceType + " RequireControl() => _started && _control is not null ? _control : throw new global::System.InvalidOperationException(NotStartedMessage);");
        builder.AppendLine("    private " + model.WorldType + " RequireWorld() => _started && _world is not null ? _world : throw new global::System.InvalidOperationException(NotStartedMessage);");
        builder.AppendLine("    private void ThrowIfDisposed() => global::System.ObjectDisposedException.ThrowIf(_disposed, this);");
    }

    private static void AppendWorldForwarders(StringBuilder builder, PluginServerFacadeModel model)
    {
        foreach (var method in model.WorldMethods)
        {
            builder.Append("    public ").Append(method.ReturnType).Append(' ').Append(method.Name)
                .Append('(').Append(ParameterList(method)).Append(") => RequireWorld().")
                .Append(method.Name).Append('(').Append(ArgumentList(method)).AppendLine(");");
        }

        if (model.WorldMethods.Count > 0)
        {
            builder.AppendLine();
        }
    }

    private static void AppendInstallSurface(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.AppendLine();
        builder.AppendLine("    public global::System.Threading.Tasks.ValueTask<TReturn> InvokeAsync<TReturn>(global::System.Func<" + model.WorldType + ", global::System.Threading.Tasks.ValueTask<TReturn>> lambda)");
        builder.AppendLine("        => throw new global::System.InvalidOperationException(\"Plugin server InvokeAsync calls must be intercepted by the DotBoxD plugin generator.\");");
        builder.AppendLine("    public global::System.Threading.Tasks.ValueTask<TReturn> InvokeAsync<TCaptures, TReturn>(TCaptures captures, global::DotBoxD.Abstractions.RemoteServerInvocation<" + model.WorldType + ", TCaptures, TReturn> lambda) where TCaptures : class");
        builder.AppendLine("        => throw new global::System.InvalidOperationException(\"Plugin server InvokeAsync calls must be intercepted by the DotBoxD plugin generator.\");");
        builder.AppendLine("    public global::DotBoxD.Abstractions.ILiveSettingsHandle<TKernel> Get<TKernel>() where TKernel : class, new()");
        builder.AppendLine("        => new LiveSettingsHandle<TKernel>(this, global::DotBoxD.Plugins.Kernel.KernelPackageRegistry.Resolve<TKernel>().Manifest.PluginId);");
        builder.AppendLine("    public string PluginId<TService>() where TService : class");
        builder.AppendLine("        => _serverExtensions.TryGetValue(typeof(TService), out var pluginId) ? pluginId : throw new global::System.InvalidOperationException($\"Server extension '{typeof(TService).FullName}' has not been registered.\");");
        builder.AppendLine("    public global::System.Threading.Tasks.ValueTask<byte[]> InvokeServerExtensionAsync(string pluginId, byte[] arguments, global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("        => RequireControl().InvokeServerExtensionAsync(pluginId, arguments, cancellationToken);");
        builder.AppendLine("    public global::System.Threading.Tasks.Task<string> EnsureAnonymousKernelAsync(string pluginId, global::System.Func<global::DotBoxD.Plugins.PluginPackage> factory)");
        builder.AppendLine("        => _anonymousKernels.GetOrAdd(pluginId, id => new global::System.Lazy<global::System.Threading.Tasks.Task<string>>(() => InstallServerExtensionPackageAsync(factory()).AsTask())).Value;");
        builder.AppendLine("    private global::System.Threading.Tasks.ValueTask<string> InstallPluginPackageAsync(global::DotBoxD.Plugins.PluginPackage package, global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("        => RequireControl().InstallPluginAsync(global::DotBoxD.Plugins.Json.PluginPackageJsonSerializer.Export(package), cancellationToken);");
        builder.AppendLine("    private global::System.Threading.Tasks.ValueTask<string> InstallSubscriptionPackageAsync(global::DotBoxD.Plugins.PluginPackage package, global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("        => RequireControl().InstallSubscriptionAsync(global::DotBoxD.Plugins.Json.PluginPackageJsonSerializer.Export(package), cancellationToken);");
        builder.AppendLine("    private global::System.Threading.Tasks.ValueTask<string> InstallServerExtensionPackageAsync(global::DotBoxD.Plugins.PluginPackage package, global::System.Threading.CancellationToken cancellationToken = default)");
        builder.AppendLine("        => RequireControl().InstallServerExtensionAsync(global::DotBoxD.Plugins.Json.PluginPackageJsonSerializer.Export(package), cancellationToken);");
    }

    private static void AppendLiveSettingsHandle(StringBuilder builder, PluginServerFacadeModel model)
    {
        builder.AppendLine("    private sealed class LiveSettingsHandle<TKernel> : global::DotBoxD.Abstractions.ILiveSettingsHandle<TKernel> where TKernel : class, new()");
        builder.AppendLine("    {");
        builder.AppendLine("        private readonly " + model.ClassName + " _owner;");
        builder.AppendLine("        private readonly string _pluginId;");
        builder.AppendLine("        private readonly global::System.Collections.Generic.List<" + model.LiveSettingUpdateType + "> _updates = new();");
        builder.AppendLine("        public LiveSettingsHandle(" + model.ClassName + " owner, string pluginId) { _owner = owner; _pluginId = pluginId; }");
        builder.AppendLine("        public global::DotBoxD.Abstractions.ILiveSettingsHandle<TKernel> Set<TValue>(global::System.Linq.Expressions.Expression<global::System.Func<TKernel, TValue>> member, TValue value)");
        builder.AppendLine("        {");
        builder.AppendLine("            var body = member.Body is global::System.Linq.Expressions.UnaryExpression unary ? unary.Operand : member.Body;");
        builder.AppendLine("            if (body is not global::System.Linq.Expressions.MemberExpression { Member: global::System.Reflection.PropertyInfo property })");
        builder.AppendLine("            {");
        builder.AppendLine("                throw new global::System.ArgumentException(\"Live setting expression must select a property.\", nameof(member));");
        builder.AppendLine("            }");
        builder.AppendLine("            if (property.GetCustomAttributes(typeof(global::DotBoxD.Abstractions.LiveSettingAttribute), inherit: true).Length == 0)");
        builder.AppendLine("            {");
        builder.AppendLine("                throw new global::System.ArgumentException($\"Property '{property.Name}' is not a live setting.\", nameof(member));");
        builder.AppendLine("            }");
        builder.AppendLine("            _updates.Add(new " + model.LiveSettingUpdateType + "(property.Name, global::System.Convert.ToString(value, global::System.Globalization.CultureInfo.InvariantCulture) ?? string.Empty));");
        builder.AppendLine("            return this;");
        builder.AppendLine("        }");
        builder.AppendLine("        public global::System.Threading.Tasks.ValueTask ApplyAsync(bool atomic = false)");
        builder.AppendLine("            => _owner.RequireControl().UpdateSettingsAsync(_pluginId, _updates.ToArray(), atomic);");
        builder.AppendLine("        public global::System.Threading.Tasks.ValueTask SetValuesAsync(global::System.Action<TKernel> set, bool atomic = false)");
        builder.AppendLine("        {");
            builder.AppendLine("            var draft = new TKernel();");
        builder.AppendLine("            set(draft);");
        builder.AppendLine("            var updates = typeof(TKernel).GetProperties(global::System.Reflection.BindingFlags.Public | global::System.Reflection.BindingFlags.Instance)");
        builder.AppendLine("                .Where(p => p.GetMethod is not null && p.GetCustomAttributes(typeof(global::DotBoxD.Abstractions.LiveSettingAttribute), inherit: true).Length != 0)");
        builder.AppendLine("                .Select(p => new " + model.LiveSettingUpdateType + "(p.Name, global::System.Convert.ToString(p.GetValue(draft), global::System.Globalization.CultureInfo.InvariantCulture) ?? string.Empty))");
        builder.AppendLine("                .ToArray();");
        builder.AppendLine("            return _owner.RequireControl().UpdateSettingsAsync(_pluginId, updates, atomic);");
        builder.AppendLine("        }");
        builder.AppendLine("    }");
    }

    private static string ParameterList(PluginServerForwardedMethod method)
        => string.Join(", ", method.Parameters.Select(p => p.Type + " @" + p.Name));

    private static string ArgumentList(PluginServerForwardedMethod method)
        => string.Join(", ", method.Parameters.Select(p => "@" + p.Name));

    private static string ShortTypeName(string fullyQualified)
    {
        var name = fullyQualified.Substring(fullyQualified.LastIndexOf('.') + 1);
        if (name.StartsWith("I", StringComparison.Ordinal) && name.Length > 1 && char.IsUpper(name[1]))
        {
            name = name.Substring(1);
        }

        return name;
    }

    private static string FieldName(string propertyName)
        => char.ToLowerInvariant(propertyName[0]) + propertyName.Substring(1);

    private static string HintName(PluginServerFacadeModel model)
        => (string.IsNullOrWhiteSpace(model.Namespace) ? model.ClassName : model.Namespace + "." + model.ClassName) +
           ".PluginServer.g.cs";
}
